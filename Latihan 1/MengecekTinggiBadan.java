class MengecekTinggiBadan
{
	/*Ini adalah program mengecek tinggi badan
	*Jika tinggi kurang dari 110 cm dinyatakan TIDAK LULUS
	*Jika tinggi sama dengan atau lebih dari 110 cm dinyatakan LULUS
	*/
	public static void main(String[] args) {
		int tb = 120;
		
		if (tb < 100) 
		{
			System.out.print("TIDAK LULUS");
		}

		else 
		{
			System.out.print("LULUS");
		}
		//Menampilkan Hasil
	}
}
